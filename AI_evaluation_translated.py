import pandas as pd
import json
import openai
import time

openai.api_key = "API_KEY"

df_sections = pd.read_csv("sections_from_books.csv")
with open("learning_goals_and_relationships_KAOS_model_gemini_.json", "r", encoding="utf-8") as f:
    goal_data = json.load(f)


def build_prompt_for_section(text, goals, soft_goals, relationships):
    goals_str = "\n".join(f"- {g}" for g in goals) if goals else "None"
    soft_goals_str = "\n".join(f"- {g}" for g in soft_goals) if soft_goals else "None"
    relationships_str = "\n".join(
        f"- {rel['goal']} -> ({rel['relation']}) -> {', '.join(rel['decomposed_to'])}"
        for rel in relationships if "decomposed_to" in rel
    ) if relationships else "None"

    prompt = f"""
 Context:

The following output was generated by a model applying the KAOS methodology for goal analysis.
The model is only allowed to extract goals (Behavioural or Soft) that:
- Belong to a single, coherent goal network
- Are connected to the main goal via AND / OR / Conflict
- Discards any goal that cannot be logically connected

 Definitions:
- Behavioural Goals: Clear, precise, and measurable goals describing what the student must do or know. Connected with actions or assignments.
- Soft Goals: Subjective or hard-to-measure goals expressing student preferences or desired qualities.
- Relationships:
  - AND: All subgoals are required to achieve the main goal.
  - OR: Achieving one or more subgoals is sufficient.
  - Conflict: Two goals contradict each other (achieving one makes the other harder).

 Important:
The absence of goals or relationships may be correct if the passage:
- Does not contain clear or measurable goals logically related to each other
- Does not allow consistent analysis

 Task:
You must evaluate the model output (i.e., its goals and relationships) based on the text.
- If no goals were given, judge whether this is justified by the passage content.
- DO NOT create your own goals or relationships.
- Evaluate ONLY what was provided.

 Respond EXACTLY in the following format. For EACH score, provide clear and fully detailed justification. If you assign a score <5, you MUST explicitly explain what is missing or wrong. Omitting justification for reduced scores invalidates the evaluation.  

Remember: goals ALWAYS refer to the student — what they will learn, understand, or be able to do after reading the passage. Not what the text itself describes as a process or application.

If you do not follow the format, the response is invalid.

Correctness: [1–5] - [justification based on the text; e.g., if goals are accurate and do not misinterpret, then 5/5; if inaccurate, assign <5 and explain exactly what is wrong]
Completeness: [1–5] - [if the goals fully cover the main points of the text, then 5/5; otherwise <5 and specify exactly what is missing]
Clarity: [1–5] - [if the goals are clearly written and understandable, then 5/5; otherwise <5 and explain what is unclear]
Relevance: [1–5] - [if goals and relationships align with the passage and KAOS methodology, then 5/5; otherwise <5 and explain why not]
Comment: [Brief overall judgment focusing on weaknesses or strengths of the output]


  TEXT:
{text}

 BEHAVIOURAL GOALS:
{goals_str}

 SOFT GOALS:
{soft_goals_str}

 EXTRACTED RELATIONSHIPS:
{relationships_str}
"""
    return prompt


results = []
for entry in goal_data:
    filename = entry['filename']
    header = entry['header']
    goals = entry.get('Behavioural_goals', [])
    soft_goals = entry.get('soft_goals', [])
    relationships = entry.get('goal_relationships', [])

    section_row = df_sections[df_sections['filename'] == filename]
    if section_row.empty:
        evaluation = "Error: Passage not found."
    else:
        section_text = section_row['section'].values[0]

        # If text is too long, split into two parts
        max_length = 10000
        if len(section_text) > max_length:
            midpoint = len(section_text) // 2
            part1 = section_text[:midpoint]
            part2 = section_text[midpoint:]

            evaluations = []
            for part in [part1, part2]:
                prompt = build_prompt_for_section(
                    text=part,
                    goals=goals,
                    soft_goals=soft_goals,
                    relationships=relationships
                )
                try:
                    response = openai.ChatCompletion.create(
                        model='gpt-3.5-turbo',
                        messages=[
                            {"role": "system", "content": "You are an evaluator of learning goals with expertise in the KAOS methodology."},
                            {"role": "user", "content": prompt}
                        ],
                        temperature=0.3
                    )
                    evaluations.append(response['choices'][0]['message']['content'])
                except Exception as e:
                    evaluations.append(f"Error: {e}")
                time.sleep(1.5)

            evaluation = "\n---\n".join(evaluations)
        else:
            prompt = build_prompt_for_section(
                text=section_text,
                goals=goals,
                soft_goals=soft_goals,
                relationships=relationships
            )
            try:
                response = openai.ChatCompletion.create(
                    model='gpt-3.5-turbo',
                    messages=[
                        {"role": "system", "content": "You are an evaluator of learning goals with expertise in the KAOS methodology."},
                        {"role": "user", "content": prompt}
                    ],
                    temperature=0.3
                )
                evaluation = response['choices'][0]['message']['content']
            except Exception as e:
                evaluation = f"Error: {e}"

    results.append({
        "filename": filename,
        "header": header,
        "evaluation": evaluation
    })

    time.sleep(1.5)

df_output = pd.DataFrame(results)
df_output.to_csv("AI_eval_of_extracted_goals_final.csv", index=False, encoding="utf-8-sig")
print(" Evaluation completed.")
